package programs_on_variables;

public class Program4 {
	public static void main(String[] args) {
		//smaller to bigger datatype
	byte b=20;//1 byte
		
		int b1=b;//4 byte
		// bigger to smaller datatype not possible but explicit typecasting can be done
		System.out.println(b1);
		int i1=30;
		byte i2=(byte)i1;
		System.out.println(i2);
		
		
		
		
		
		
	}
	
	

}
