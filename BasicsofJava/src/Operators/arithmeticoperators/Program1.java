package Operators.arithmeticoperators;

public class Program1 {
	//Arithmetic operator
	public static void main(String[] args) {
		// Addition
		//normal , Concatenation
		int a=10;
		int b=30;
		System.out.println(10+20);
		System.out.println(a+b);
		
		//Property
		/*
		 Every operator will have precedence and Associavity
		 
		 Every Return Value and Type of the Value
		 */
		
		System.out.println(" "+10+10);
		System.out.println(10+20+"Hello"+100);//Concatenation (merging of string)
		System.out.println(10+30*22);
		System.out.println(5/2.0);
		System.out.println('A'+'B');//131 as ascii values
		System.out.println(10+20+"HELLO");	
		
				
		
		// int/double-->int
		
	}

}
