public class Demo {
	/*
	 * Every class name is non-primitive data type
	 */
	static int a=20;
	public static void main(String[] args) {
		/*
		 * syntax to access static member
		 * ClassName.member name;
		 */
		int a=30;
		System.out.println(a);
		System.out.println(Demo.a);

	}


	

}
